	public void setReTestUnpassNo(int reTestUnpassNo) {
		this.reTestUnpassNo = reTestUnpassNo;
	}
	
	public int getDangerUnpassNo() {
		return dangerUnpassNo;
	}
	
	public void setDangerUnpassNo(int dangerUnpassNo) {
		this.dangerUnpassNo = dangerUnpassNo;
	}
